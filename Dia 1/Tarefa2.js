/** Crie duas variáveis numero1 e numero2
 * 
 * Mostre no console:
 *  A soma dos dois
 *  A subtração
 *  A multiplicação
 *  A divisão
 */

let numero1 = 5;
let numero2 = 3;

console.log(numero1 + " + " + numero2 + " = " + (numero1 + numero2))
console.log(numero1 + " - " + numero2 + " = " + (numero1 - numero2))
console.log(numero1 + " * " + numero2 + " = " + (numero1 * numero2))
console.log(numero1 + " / " + numero2 + " = " + (numero1 / numero2))

// Perfect
// Sem duvidas
